package org.biins.objectbuilder.classes;

/**
 * @author Martin Janys
 */
public class PrimitiveObject {

    private boolean bool;
    protected byte b;
    char c;
    public double d;
    private float f;
    private int i;
    private long l;

    public float getF() {
        return f;
    }

    public void setF(float f) {
        this.f = f;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public long getL() {
        return l;
    }

    public void setL(long l) {
        this.l = l;
    }
}
